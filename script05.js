const nome = "Rocco"; // Declaração da variável 'nome' e atribuição do valor "Rocco"
const novoNome = nome.toLocaleLowerCase(); // Declaração da variável 'novoNome' e atribuição do valor de 'nome' convertido para minúsculas

console.log(nome.toLocaleUpperCase()); // Imprime "ROCCO" no console, convertendo 'nome' para maiúsculas temporariamente
console.log(nome); // Imprime "Rocco" no console, mostrando o valor original da variável 'nome'
console.log(); // Imprime uma linha em branco no console
console.log(novoNome); // Imprime "rocco" no console, mostrando o valor de 'nome' convertido para minúsculas
