// Declarando uma variável 'n1' e atribuindo o valor 2 a ela
let n1 = 2;

// Declarando uma variável 'n2' e atribuindo o valor 1 a ela
let n2 = 1;

// Verificando se 'n1' é maior que 'n2'
if (n1 > n2) {
  // Se a condição for verdadeira, imprime "n1 é maior que n2" no console
  console.log("n1 é maior que n2");
} else if (n1 === n2) {
  // Se a condição anterior for falsa, verifica se 'n1' é igual a 'n2'
  // Se esta condição for verdadeira, imprime "eles são iguais" no console
  console.log("eles são iguais");
} else {
  // Se todas as condições anteriores forem falsas, imprime "n2 é maior que n1" no console
  console.log("n2 é maior que n1");
}
